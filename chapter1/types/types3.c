// Create an integer variable, assign a value, and print
#include <stdio.h>

int main(void)
{
    // Initalize x and y
    int x = 13;
    int y = 67;

    // Update the values of x and y
    x = 5;
    y = y + 4;


    // Print out the current value of x
    printf("The current value of x is %i and the current value of y is %i.\n", x, y);
}
